#include <stdio.h>

int main()
{
  // 1. 변수 선언 (자료형 3개 이상, 변수 5개 이상)
  char itemName[20];    // 상품명 (문자열)
  int Price;            // 상품 가격 (정수)
  int count;            // 판매 수량 (정수)
  int pay;              // 손님에게 받은 돈 (정수)
  int total;            // 총 결제 금액 (정수)
  double taxRate = 0.1; // 부가세율 10% (실수)

  printf("========= GS25 편의점 POS 시스템 V1.0 =========\n");
  printf("신입 알바생님, 환영합니다! 계산을 시작합니다.\n\n");

  // 2. 입력 (scanf 사용)
  printf("1. 판매할 상품명 입력: ");
  scanf("%s", itemName);

  printf("2. 상품 단가 입력(원): ");
  scanf("%d", &Price);

  printf("3. 판매 수량 입력(개): ");
  scanf("%d", &count);

  // 3. 산술 연산 (가격 * 수량)
  total = Price * count;

  // 4. 결과 출력
  printf("\n----------------------------------------------\n");
  printf("상품명: %s\n", itemName);
  printf("총 결제 금액: %d원 (부가세 포함)\n", total);
  printf("----------------------------------------------\n");

  // 5. 거스름돈 계산을 위한 추가 입력
  printf("손님에게 받은 금액을 입력하세요: ");
  scanf("%d", &pay);

  // 마지막 산술 연산 및 출력 (받은 돈 - 총액)
  printf("\n>>> 거스름돈: %d원입니다.\n", pay - total);
  printf("==============================================\n");
  printf("결제가 완료되었습니다. 오늘도 화이팅하세요!\n");
return 0;
}
